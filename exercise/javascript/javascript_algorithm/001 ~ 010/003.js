/*
    문제3. 변수의 타입

    다음 출력 값으로 올바른 것은?

    var arr = [100, 200, 300];
    console.log(typeof(arr));

    1) undefined
    2) string
    3) number
    4) object
*/

// 답안
var arr = [100, 200, 300];
console.log(typeof(arr)); // 출력값은 object이므로 4번

/*
    - 풀이
    
    1. undefined = 값이 할당되지 않은 상태
    2. string = 문자열
    3. number = 숫자형
*/